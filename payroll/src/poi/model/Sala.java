package poi.model;
//生成的Excel数据的实体类
public class Sala {
	private int id;
	private String number;
	private String name;
	private String dept_name;
	private String level;
	private int base;
	private int subsidys;
	private int prizes;
	private int checks;
	private int yfgz;
	private int sfgz;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDept_name() {
		return dept_name;
	}
	public void setDept_name(String dept_name) {
		this.dept_name = dept_name;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public int getBase() {
		return base;
	}
	public void setBase(int base) {
		this.base = base;
	}
	public int getSubsidys() {
		return subsidys;
	}
	public void setSubsidys(int subsidys) {
		this.subsidys = subsidys;
	}
	public int getPrizes() {
		return prizes;
	}
	public void setPrizes(int prizes) {
		this.prizes = prizes;
	}
	public int getChecks() {
		return checks;
	}
	public void setChecks(int checks) {
		this.checks = checks;
	}
	public int getYfgz() {
		return yfgz;
	}
	public void setYfgz(int yfgz) {
		this.yfgz = yfgz;
	}
	public int getSfgz() {
		return sfgz;
	}
	public void setSfgz(int sfgz) {
		this.sfgz = sfgz;
	}
	
	
}
